  /* Write Data in Database*/
  Future<List<Jour>> allDay() async {
    Database myDb = await database;
    List<Map<String, dynamic>> resultat =
        await myDb.rawQuery(' SELECT * FROM jour ');
    List<Jour> days = [];
    resultat.forEach((map) {
      Jour day = new Jour();
      day.fromMap(map);
      days.add(day);
    });
    return days;
  }
